// ── Helper: generate a 30-point price history with drift toward `end` ──
function priceHistory(start: number, end: number, seed: number): number[] {
  let s = seed;
  const next = () => {
    s = (s * 1103515245 + 12345) & 0x7fffffff;
    return (s >>> 16) / 32767;
  };
  const pts: number[] = [start];
  for (let i = 1; i < 30; i++) {
    const t = i / 29;
    const target = start + (end - start) * t;
    const noise = (next() - 0.5) * 12;
    pts.push(Math.max(1, Math.min(99, Math.round(target + noise))));
  }
  pts[29] = end;
  return pts;
}

